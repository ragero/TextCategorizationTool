    private String[] getJList(){
        String[] string = new String[simpleRanking_Parameters.getKs().size()];
        for (int i = 0; i < simpleRanking_Parameters.getKs().size(); i++) {
            string[i] = simpleRanking_Parameters.getKs().get(i).toString();
        }
        return string;
    }
    
