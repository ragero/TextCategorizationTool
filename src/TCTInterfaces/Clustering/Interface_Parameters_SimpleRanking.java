    private void bOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOkActionPerformed
        ArrayList<Integer> ks = new ArrayList<Integer>();
        ListModel model = lGroups.getModel();
        for(int i = 0; i < model.getSize(); i++){
            ks.add(Integer.parseInt(model.getElementAt(i).toString()));
        }
        
        ArrayList<Double> alfas = new ArrayList<Double>();
        ListModel model2 = lGroups1.getModel();
        for(int i = 0; i < model2.getSize(); i++){
            alfas.add(Double.parseDouble(model2.getElementAt(i).toString()));
        }
        if(rAutomatic.isSelected()){
            simpleRanking_Parameters.setIncrementK(Integer.parseInt(tIncrement.getText()));
            simpleRanking_Parameters.setAutoIncrementK(true);
        }else{
            simpleRanking_Parameters.setAutoIncrementK(false);
            simpleRanking_Parameters.setKs(ks);
        }
        simpleRanking_Parameters.setMinimumDifference(Double.parseDouble(tCohesion.getText()));
        simpleRanking_Parameters.setMaxIteration(Integer.parseInt(tMaxIterations.getText()));
        simpleRanking_Parameters.setAlfa(alfas);
        this.dispose();
    }//GEN-LAST:event_bOkActionPerformed

