    private void bOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOKActionPerformed
        if(rWeight.isSelected()){
            lp_Parameters.setWeighted(true);
        }else{
            lp_Parameters.setWeighted(false);
        }
        if(rUnweighted.isSelected()){
            lp_Parameters.setUnitary(true);
        }else{
            lp_Parameters.setUnitary(false);
        }
        if(rSynchronous.isSelected()){
            lp_Parameters.setSynchronous(true);
        }else{
            lp_Parameters.setSynchronous(false);
        }
        if(rAssynchronous.isSelected()){
            lp_Parameters.setAsynchronous(true);
        }else{
            lp_Parameters.setAsynchronous(false);
        }
        
        lp_Parameters.setMaxIteration(Integer.parseInt(tIterations.getText()));
        this.dispose();
    }//GEN-LAST:event_bOKActionPerformed

